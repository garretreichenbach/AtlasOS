--[[ Public-domain font8x8 basic (U+0000–U+007F). See dhepper/font8x8 on GitHub. ]]
local function glyph_from_hex16(s16)
  local g = {}
  for i = 1, 8 do
    g[i] = tonumber(s16:sub(i * 2 - 1, i * 2), 16) or 0
  end
  return g
end

